<script>
    $(document).ready(function() {
        // Hàm để chuyển tên sản phẩm thành slug
        function slugify(text) {
            return text.toString().toLowerCase()
                .replace(/\s+/g, '-')           // Thay khoảng trắng bằng dấu gạch ngang
                .replace(/[^\w\-]+/g, '')       // Loại bỏ các ký tự không phải chữ, số, dấu gạch ngang
                .replace(/\-\-+/g, '-')         // Thay nhiều dấu gạch ngang bằng một dấu gạch ngang
                .replace(/^-+/, '')             // Loại bỏ dấu gạch ngang ở đầu
                .replace(/-+$/, '');            // Loại bỏ dấu gạch ngang ở cuối
        }

        // Hàm tìm kiếm
        $('#search-input').on('keyup', function() {
            let query = $(this).val();

            if (query.length > 2) {  // Bắt đầu tìm kiếm khi người dùng nhập từ 3 ký tự trở lên
                $.ajax({
                    url: '/api/search',  // Địa chỉ API để lấy dữ liệu tìm kiếm
                    type: 'GET',
                    data: { search: query },
                    success: function(response) {
                        $('#search-results').empty().show();
                        if (response.length > 0) {
                            response.forEach(item => {
                                let formattedPrice = parseFloat(item.price_sale).toLocaleString('vi-VN', { style: 'currency', currency: 'VND' });
                                let productUrl = `/san-pham/${item.id}-${slugify(item.name)}.html`;
                                $('#search-results').append(`
                                    <div class="search-result-item" style="padding: 10px; border-bottom: 1px solid #ccc; display: flex; align-items: center;">
                                        <a href="${productUrl}" style="display: flex; align-items: center; text-decoration: none; color: inherit;">
                                            <img src="${item.thumb}" alt="${item.name}" style="width: 50px; height: 50px; margin-right: 10px;">
                                            <div>
                                                <strong>${item.name}</strong><br>
                                                <span>${formattedPrice}</span>
                                            </div>
                                        </a>
                                    </div>
                                `);
                            });
                            $('#search-results').addClass('search-container'); // Thêm class cho phần tìm kiếm
                        } else {
                            $('#search-results').append('<div style="padding: 10px;">Không tìm thấy kết quả</div>');
                        }
                    }
                });
            } else {
                $('#search-results').hide();
            }
        });

        // Ẩn kết quả tìm kiếm khi người dùng di chuột ra khỏi thanh tìm kiếm
        $('#sosearchpro').mouseleave(function() {
            $('#search-results').hide();
        });
    });
</script>

